void rush(int x, int y){
	char arr[x][y];
	int i = 0;
	int j = 0;
	int k = 0;
	int l = 0;


	/************************/
	while(j < y){
		while(i < x){
			if(j == 0 || j == y - 1){

				if (i != 0 && i != x - 1){

					arr[i][j] = '*';

				}else{

					if((i > 0 && j > 0) && ((i == 0 && j == 0) || (i == x - 1 && j == y - 1))){
						arr[i][j] = '/';
					}else{
						arr[i][j] = '\\';
					}
					arr[0][0] = '/';
				}
			}else{
				if(i != 0 && i != x - 1){
					arr[i][j] = ' ';
				}else{
					arr[0][j] = '*';
					arr[x - 1][j] = '*';
				}

			}
			i++;
		}
		i = 0;
		j++;
	}
	/*************************/
	while(l < y){
		while(k < x){
			ft_putchar(arr[k][l]);
			k++;
		}
		k = 0;

		ft_putchar('\n');

		l++;
	}

}
