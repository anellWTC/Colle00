void rush(int x, int y){
	char arr[x][y];
	int i = 0;
	int j = 0;
	int k = 0;
	int l = 0;


	/************************/
	while(j < y){
		while(i < x){
			if(j == 0 || j == y - 1){

				if (i != 0 && i != x - 1){

					arr[i][j] = 'B';

				}else{

					if(i == 0){
						arr[i][j] = 'A';
					}else{
						arr[i][j] = 'C';
					}
				}
			}else{
				if(i != 0 && i != x - 1){
					arr[i][j] = ' ';
				}else{
					arr[0][j] = 'B';
					arr[x - 1][j] = 'B';
				}

			}
			i++;
		}
		i = 0;
		j++;
	}
	/*************************/
	while(l < y){
		while(k < x){
			ft_putchar(arr[k][l]);
			k++;
		}
		k = 0;

		ft_putchar('\n');

		l++;
	}

}
